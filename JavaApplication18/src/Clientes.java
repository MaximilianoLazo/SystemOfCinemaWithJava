    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        Conectar con = new Conectar();
        Connection cn = (Connection) con.conexion();
      
        
        try{
        if ("".equals(txtdni.getText())|| "".equals(txtnombre.getText())|| "".equals(txttel.getText())|| "".equals(txtpuntos.getText())){
            JOptionPane.showMessageDialog(null, "Faltan completar campos","OcurriÃ³ un error",JOptionPane.INFORMATION_MESSAGE); 
       
       }else{
         PreparedStatement pst = (PreparedStatement) cn.prepareStatement("insert into cliente (dni_cliente,datos_cliente,contacto_cliente,puntos_cliente) values (?,?,?,?)");
        pst.setString(1,txtdni.getText()); 
        pst.setString(2,txtnombre.getText()); 
        pst.setString(3,txttel.getText());
        pst.setString(4,txtpuntos.getText()); 
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Nuevo cliente creado","Mensaje",JOptionPane.INFORMATION_MESSAGE);
        limpiar();
        mostrardatosTabla("");
        jButton2.setEnabled(false);
        jButton3.setEnabled(false);
        jButton1.setEnabled(true);
        jTextField1.setEnabled(true);
        
        
        }
       
        
        
        } catch (SQLException e){
            java.util.logging.Logger.getLogger(Usuario.class.getName()) .log(java.util.logging.Level.SEVERE,null,e);
            JOptionPane.showMessageDialog(rootPane, e);
            JOptionPane.showMessageDialog(null, "Error SQL Consultar"+e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

