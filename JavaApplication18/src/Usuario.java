    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Conectar con = new Conectar();
        Connection cn = (Connection) con.conexion();
        // BOTON GUARDAR
        try{
            if ("".equals(jUsuario.getText())|| "".equals(jContrasenia.getText())){
            JOptionPane.showMessageDialog(null, "Faltan completar campos","Ocurrió un error",JOptionPane.INFORMATION_MESSAGE);  
            }else{
             PreparedStatement pst = (PreparedStatement) cn.prepareStatement("insert into usuario (Usuario,Contrasenia,id_tipoUsuario) values (?,?,?)");
            pst.setString(1,jUsuario.getText()); 
            pst.setString(2,Hash.getHash(jContrasenia.getText(), "SHA1"));
            pst.setString(3,(String) jTipoUsuario.getSelectedItem());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Nuevo usuario creado","Mensaje",JOptionPane.INFORMATION_MESSAGE);
            mostrardatosTabla("");
            limpiar();
            jButton5.setEnabled(false);
            jButton3.setEnabled(true);
            jTextField1.setEnabled(true);
            jButton2.setEnabled(false);
            jButton4.setEnabled(true);
            
            
            }
        } catch (SQLException e){
            java.util.logging.Logger.getLogger(Usuario.class.getName()) .log(java.util.logging.Level.SEVERE,null,e);
            JOptionPane.showMessageDialog(rootPane, e);
            JOptionPane.showMessageDialog(null, "Error SQL Consultar"+e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

