     public void buscardatosTabla(String Valor){
        Conectar conn = new Conectar();
        conn.conexion();
        DefaultTableModel modelo;
        modelo = new DefaultTableModel();
        modelo.addColumn ("ID");
        modelo.addColumn ("Usuario");
        modelo.addColumn ("Contraseña");
        modelo.addColumn ("Tipo usuario");

        String sql;
        
        if(Valor.equals("")){
        sql= "select id_usuario, Usuario,Contrasenia,id_tipoUsuario from usuario";
        }
        else{
            sql = "select id_usuario, Usuario, Contrasenia,id_tipoUsuario from usuario where  Usuario like '%"+Valor+"%'";
        }
        String[] datos = new String[4];
        com.mysql.jdbc.Statement st;
        
        
        try{
            ResultSet rs = (ResultSet) conn.Consultar(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);

            modelo.addRow(datos);
            }
            jTable1.setModel(modelo);
        } catch(SQLException ex){
            java.util.logging.Logger.getLogger(Usuario.class.getName()) .log(java.util.logging.Level.SEVERE,null,ex);
        }
    }
     
